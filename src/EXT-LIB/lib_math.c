/*
*********************************************************************************************************
* Description: 最大最小值
*
* Arguments  : none.
* 
* Return     : 
*********************************************************************************************************
*/

s16 math_imin(s16 a, s16 b) compact reentrant
{
    return a < b ? a : b;
}

u16 math_uimin(u16 a, u16 b) compact reentrant
{
    return a < b ? a : b;
}

s16 math_imax(s16 a, s16 b) compact reentrant
{
    return a > b ? a : b;
}

u16 math_uimax(u16 a, u16 b) compact reentrant
{
    return a > b ? a : b;
}

