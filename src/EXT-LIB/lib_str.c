/*
*********************************************************************************************************
*                                          str_format()
*
* Description: 格式化字符串
*
* Arguments  : none.
* 
* Return     : 
*
* Note(s)    : none. 未完成
*********************************************************************************************************
*/
int str_format(char *buf, char *format, ...) compact reentrant
{
    va_list aptr;
    int ret;

    va_start(aptr, format);
    ret = vsprintf(buf, format, aptr);
    va_end(aptr);

    return ret;
}
