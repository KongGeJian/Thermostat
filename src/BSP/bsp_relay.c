/*
*********************************************************************************************************
*                                               DEFINE
*********************************************************************************************************
*/

#define DD  P11     //驱动：初始拉高，0-驱动MOS管导通。输出脉冲信号

#define DD_Start()   DD=1  //驱动启动
#define DD_Stop()    DD=0  //驱动停止
#define DD_IsStart() DD==1 //驱动是否启动中



/*
*********************************************************************************************************
*                                             INTERRUPT
*********************************************************************************************************
*/


/*
*********************************************************************************************************
* Description : 焊接初始化
*
* Caller(s)   : BSP_Init().
*
* Note(s)     : none.
*********************************************************************************************************
*/
void BSP_RELAY_Init(void) large
{
    P1M1 &= ~0x02;      //DD设置为推挽输出
    P1M0 |= 0x02;

    DD_Stop();
}

/*
*********************************************************************************************************
* Description : 启动
*
* Note(s)     : none.
*********************************************************************************************************
*/
void BSP_RELAY_Start() large
{
    DD_Start();
}

/*
*********************************************************************************************************
* Description : 停止
*
* Note(s)     : none.
*********************************************************************************************************
*/
void BSP_RELAY_Stop() large
{
    DD_Stop();
}

